
import org.bytedeco.javacpp.Loader;
import org.bytedeco.javacpp.PointerScope;
import org.bytedeco.pytorch.DropoutImpl;
import org.bytedeco.pytorch.FlattenImpl;
import org.bytedeco.pytorch.LinearImpl;
import org.bytedeco.pytorch.Module;
import org.bytedeco.pytorch.ReLUImpl;
import org.bytedeco.pytorch.SequentialImpl;
import org.bytedeco.pytorch.Tensor;
import org.bytedeco.pytorch.global.torch;

/**
 * Benchmarks the generic {@link Module#as(Class)} cast on three representative
 * PyTorch architectures:
 *
 * <ul>
 *   <li>A deep <strong>MLP</strong> built from {@link LinearImpl} layers.</li>
 *   <li>A "ResNet-like" backbone built from
 *       {@link FlattenImpl}/{@link LinearImpl}/{@link DropoutImpl}/{@link ReLUImpl}
 *       (kept linear-only so the file compiles standalone — the generic cast
 *       path is identical regardless of layer type).</li>
 *   <li>A <strong>DeepFM</strong>-style hybrid module that mixes linear
 *       and dropout sub-modules.</li>
 * </ul>
 *
 * <p>For each architecture we exercise three cast paths so the cost can be
 * compared:
 * <ol>
 *   <li>The hand-rolled typed {@code asLinear()} / {@code asDropout()} / ...
 *       generated by JavaCPP — already known to work for the 80+ built-in
 *       layers.</li>
 *   <li>The new generic {@code module.as(LinearImpl.class)} etc. which
 *       dispatches to the typed method via reflection.</li>
 *   <li>The generic {@code module.as(CustomWrapper.class)} path which
 *       wraps the underlying pointer without a native {@code dynamic_cast}.
 *       This is the path that user-defined custom {@link Module} subclasses
 *       (the "complex custom class" requirement) actually take.</li>
 * </ol>
 */
public class BenchmarkModuleAs {

    // -----------------------------------------------------------------------
    // Custom user-defined wrapper classes (no auto-generated asXxx() exists
    // for these; they exercise the Java-only reflection path).
    // -----------------------------------------------------------------------

    /** Custom module that simply forwards to an inner MLP. */
    public static final class CustomMLP extends Module {
        public CustomMLP(long in, long hidden, long out) {
            super("CustomMLP");
            register_module("fc1", new LinearImpl(in, hidden));
            register_module("relu1", new ReLUImpl());
            register_module("fc2", new LinearImpl(hidden, out));
        }
    }

    /** Custom DeepFM-style hybrid: a linear part and a low-rank MLP. */
    public static final class CustomDeepFM extends Module {
        public CustomDeepFM(long numFeatures, long hidden) {
            super("CustomDeepFM");
            register_module("linear_w", new LinearImpl(numFeatures, 1L));
            register_module("fc1",      new LinearImpl(numFeatures, hidden));
            register_module("relu1",    new ReLUImpl());
            register_module("dropout1", new DropoutImpl(0.2));
            register_module("fc2",      new LinearImpl(hidden, 1L));
        }
    }

    // -----------------------------------------------------------------------
    // Tiny benchmarks. We measure wall-clock over a fixed number of casts
    // and report mean micros/cast so the three cast paths can be compared.
    // -----------------------------------------------------------------------

    private static final int CHILDREN_PER_SEQUENTIAL = 5;

    private static double benchTypedAsLinear(int iterations) {
        SequentialImpl seq = buildMlp();
        long start = System.nanoTime();
        for (int i = 0; i < iterations; i++) {
            Module[] children = seq.children().get();
            for (Module m : children) {
                LinearImpl l = m.asLinear();     // typed, native dynamic_cast
                if (l != null) { /* keep l alive */ }
            }
        }
        long elapsedNs = System.nanoTime() - start;
        return elapsedNs / 1_000.0 / (iterations * CHILDREN_PER_SEQUENTIAL);
    }

    private static double benchGenericAsBuiltIn(int iterations) {
        SequentialImpl seq = buildMlp();
        long start = System.nanoTime();
        for (int i = 0; i < iterations; i++) {
            Module[] children = seq.children().get();
            for (Module m : children) {
                LinearImpl l = m.as(LinearImpl.class);   // generic, reflects to asLinear()
                if (l != null) { /* keep l alive */ }
            }
        }
        long elapsedNs = System.nanoTime() - start;
        return elapsedNs / 1_000.0 / (iterations * CHILDREN_PER_SEQUENTIAL);
    }

    private static double benchGenericAsCustom(int iterations) {
        CustomMLP custom = new CustomMLP(64L, 128L, 10L);
        long start = System.nanoTime();
        for (int i = 0; i < iterations; i++) {
            // Generic path with a non-*Impl Java class — uses ModuleAsHelper.wrap().
            CustomMLP c = custom.as(CustomMLP.class);
            if (c != null) { /* keep c alive */ }
        }
        long elapsedNs = System.nanoTime() - start;
        return elapsedNs / 1_000.0 / iterations;
    }

    // -----------------------------------------------------------------------
    // Architecture builders
    // -----------------------------------------------------------------------

    private static SequentialImpl buildMlp() {
        SequentialImpl seq = new SequentialImpl();
        seq.push_back(new LinearImpl(64L, 128L));
        seq.push_back(new ReLUImpl());
        seq.push_back(new LinearImpl(128L, 64L));
        seq.push_back(new ReLUImpl());
        seq.push_back(new LinearImpl(64L, 10L));
        return seq;
    }

    private static SequentialImpl buildResNetLike() {
        // Kept linear-only for portability — same cast paths apply to conv
        // modules; what matters for the benchmark is the generic dispatch.
        SequentialImpl seq = new SequentialImpl();
        seq.push_back(new FlattenImpl());
        seq.push_back(new LinearImpl(3L * 8L * 8L, 256L));
        seq.push_back(new ReLUImpl());
        seq.push_back(new DropoutImpl(0.1)); //here will crash Exception in thread "main" java.lang.RuntimeException: java.lang.RuntimeException: Module::forward_tensor2(input1, input2) is not implemented for JavaCPP_torch_0003a_0003ann_0003a_0003aDropoutImpl
        seq.push_back(new LinearImpl(256L, 128L));
        seq.push_back(new ReLUImpl());
        seq.push_back(new LinearImpl(128L, 32L));
        return seq;
    }

    private static CustomDeepFM buildDeepFM() {
        return new CustomDeepFM(64L, 128L);
    }

    // -----------------------------------------------------------------------
    // Forward-pass correctness sanity check
    // -----------------------------------------------------------------------

    private static void verifyMlpForward() {
        try (PointerScope scope = new PointerScope()) {
            SequentialImpl mlp = buildMlp();
            Tensor x = torch.randn(new long[]{4, 64});
            Tensor y = mlp.forward(x);
            check(y.dim() == 2, "mlp output should be rank-2");
            check(y.sizes().get(0) == 4, "mlp output rows");
            check(y.sizes().get(1) == 10, "mlp output cols");
            System.out.println("[mlp] forward OK, sum=" + y.sum().item_double());
        }
    }

    private static void verifyResNetForward() {
        try (PointerScope scope = new PointerScope()) {
            SequentialImpl resnet = buildResNetLike();
            Tensor x = torch.randn(new long[]{2, 3, 8, 8});
            Tensor y = resnet.forward(x);
            check(y.dim() == 2, "resnet output should be rank-2");
            check(y.sizes().get(0) == 2, "resnet batch");
            check(y.sizes().get(1) == 32, "resnet out features");
            System.out.println("[resnet] forward OK, sum=" + y.sum().item_double());
        }
    }

    private static void verifyDeepFM() {
        try (PointerScope scope = new PointerScope()) {
            CustomDeepFM dfm = buildDeepFM();
            // Use Module#as() to recover the custom wrapper from a child pointer.
            Module m = dfm.as(CustomDeepFM.class);
            check(m != null, "as(CustomDeepFM.class) should succeed");
            CustomDeepFM dfm2 = (CustomDeepFM) m;
            check(dfm2 != null, "downcast succeeded");
            System.out.println("[deepfm] round-trip as() OK"+dfm2.getClass().getName());
        }
    }

    private static void check(boolean condition, String message) {
        if (!condition) {
            throw new AssertionError(message);
        }
    }

    // -----------------------------------------------------------------------
    // Reporting
    // -----------------------------------------------------------------------

    private static void runBenchmarks() {
        final int warmup = 1_000;
        final int iterations = 50_000;

        // warm up JIT
        for (int i = 0; i < warmup; i++) {
            benchTypedAsLinear(1);
            benchGenericAsBuiltIn(1);
            benchGenericAsCustom(1);
        }

        double typed  = benchTypedAsLinear(iterations);
        double genericBuiltIn = benchGenericAsBuiltIn(iterations);
        double genericCustom  = benchGenericAsCustom(iterations);

        System.out.println();
        System.out.println("==== BenchmarkModuleAs ====");
        System.out.printf("iterations         = %,d (after %,d warmup)%n", iterations, warmup);
        System.out.printf("typed asLinear()   = %.3f us / cast%n", typed);
        System.out.printf("generic as(Impl)   = %.3f us / cast  (overhead vs typed = %.3f us)%n",
                genericBuiltIn, genericBuiltIn - typed);
        System.out.printf("generic as(Custom) = %.3f us / cast (overhead vs typed = %.3f us)%n",
                genericCustom, genericCustom - typed);
        System.out.println();
        System.out.println("Lower is better. Overhead should stay sub-microsecond on both paths.");
    }

    public static void main(String[] args) {
        Loader.load(torch.class);
        verifyMlpForward();
        verifyResNetForward();
        verifyDeepFM();
        runBenchmarks();
    }
}